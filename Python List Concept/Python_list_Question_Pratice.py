# Create a list of five fruits and print it.
fruit =  ["apple" , "grapes" , "banana" , "mango" , "kiwi"]
print(fruit)

# Print the first element.
print(fruit[0])

#Print the last element without using its index number directly
print(fruit[-4])

#Print the length of the list.
print(len(fruit))

#Print every element using a for loop.
for i in fruit:
    print(i)